mood = input("What is your mood today?")
if mood == "happy":
    print("Great so see you happy.")
elif mood == "nervous":
    print("Take a deep breath 3 times.")
elif mood == "sad":
    print("Cheer up man")
elif mood == "excited":
    print("Yeah, that the spirit")
elif mood == "relaxed":
    print("Great, let's start then")
else:
    print("I do not recognize this mood")